        public static void newMap()
        {
            int count = 0;
            foreach (Char i in map)
            {
                map[count] = ' ';
                count++;
            }
            Player.Position = random.Next(1919);
            map[Player.Position] = Player.Char;
            populateEnemies(numberOfEnemies);
            Screen.print();
        }
        private static void populateEnemies(int number)
        {
            int i = 0;
            while(i < number)
            {
                tileEnemy.Save(random.Next(1919));
                i++;
            }
        }
        









        public static void listen()
        {
            if(KeyListener.keyinfo.Key == ConsoleKey.DownArrow)
            {
                if(Player.Position <= 1839)
                {
                    Player.Position += 80;
                }
                lightUp();
                WorldGen.map[Player.Position] = Player.Char;
                Screen.print();
            }
            if (KeyListener.keyinfo.Key == ConsoleKey.UpArrow)
            {
                if (Player.Position >= 80)
                {
                    Player.Position -= 80;
                }
                lightUp();
                WorldGen.map[Player.Position] = Player.Char;
                Screen.print();
            }
            if(KeyListener.keyinfo.Key == ConsoleKey.LeftArrow)
            {
                if (Player.Position >= 1)
                {
                    Player.Position--;
                }
                lightUp();
                WorldGen.map[Player.Position] = Player.Char;
                Screen.print();
            }
            if (KeyListener.keyinfo.Key == ConsoleKey.RightArrow)
            {
                if (Player.Position <= 1918)
                {
                    Player.Position++;
                }
                lightUp();
                WorldGen.map[Player.Position] = Player.Char;
                Screen.print();
            }
            if (KeyListener.keyinfo.Key == ConsoleKey.Escape)
            {
                Program.CurrentMap = Menu.map;
                Screen.print();
                Program.SaveGame();
            }
        }
        private static void lightUp()
        {
            foreach(int i in numberOfLightPos)
            {
                tilePath.Place(Player.Position + i);
                tilePath.Place(Player.Position - i);
            }
            foreach(int i in numberOfLightPos)
            {
                foreach (int j in tileEnemy.Enemies)
                {
                    if ((Player.Position - i) == j)
                    {
                        tileEnemy.Place(j);
                    }
                    else if ((Player.Position + i) == j)
                    {
                        tileEnemy.Place(j);
                    }
                }
            }
        }
