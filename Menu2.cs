        public static void listen()
        {
            
            if (KeyListener.keyinfo.Key == ConsoleKey.DownArrow)
            {
                if (map[MintGame] == Program.SelectChar)
                {
                    selectLoadGame();
                }
                else if (map[LoadGame] == Program.SelectChar)
                {
                    selectReverse();
                }
                else if (map[Reverse] == Program.SelectChar)
                {
                    selectMintGame();
                }
            }
            
            if (KeyListener.keyinfo.Key == ConsoleKey.UpArrow)
            {
                if (map[MintGame] == Program.SelectChar)
                {
                    selectReverse();
                }
                else if (map[LoadGame] == Program.SelectChar)
                {
                    selectMintGame();
                }
                else if (map[Reverse] == Program.SelectChar)
                {
                    selectLoadGame();
                }
            }
            
            if (KeyListener.keyinfo.Key == ConsoleKey.Enter)
            {
                if (map[MintGame] == Program.SelectChar)
                {
                    WorldGen.newMap();
                    Program.CurrentMap = WorldGen.map;
                    Screen.print();
                }
                else if(map[LoadGame] == Program.SelectChar)
                {
                    Program.LoadGame();
                }
                else if (map[Reverse] == Program.SelectChar)
                {
                    Program.CurrentMap = Menu.map;
                    Screen.print();
                }
            }
            if (KeyListener.keyinfo.Key == ConsoleKey.Escape)
            {
                Program.CurrentMap = Menu.map;
                Screen.print();
            }
        }
        private static int MintGame = 600;
        private static int LoadGame = 840;
        private static int Reverse = 1079;
        private static void selectMintGame()
        {
            map[MintGame] = Program.SelectChar;
            map[LoadGame] = ' ';
            map[Reverse] = ' ';

            map[MintGame - 10] = Program.SelectChar2;
            map[LoadGame - 10] = ' ';
            map[Reverse - 8] = ' ';
            Screen.print();
        }
        private static void selectLoadGame()
        {
            map[MintGame] = ' ';
            map[LoadGame] = Program.SelectChar;
            map[Reverse] = ' ';


            map[MintGame - 10] = ' ';
            map[LoadGame - 10] = Program.SelectChar2;
            map[Reverse - 8] = ' ';
            Screen.print();
        }
        private static void selectReverse()
        {
            map[MintGame] = ' ';
            map[LoadGame] = ' ';
            map[Reverse] = Program.SelectChar;

            map[MintGame - 10] = ' ';
            map[LoadGame - 10] = ' ';
            map[Reverse - 8] = Program.SelectChar2;
            Screen.print();
        }
